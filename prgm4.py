#GRADE CHECKER
mark = int(input("ENTER THE MARK: "))
if mark >= 95:
    print("GRADE: S")
elif mark >= 90:
    print("GRADE: A+")
elif mark >= 85:
    print("GRADE: A")
elif mark >= 80:
    print("GRADE: B+")
elif mark >= 75:
    print("GRADE: B")
elif mark >= 70:
    print("GRADE: C+")
elif mark >= 65:
    print("GRADE: C")
elif mark >= 50:
    print("GRADE: D")
else:
    print("GRADE: F")
